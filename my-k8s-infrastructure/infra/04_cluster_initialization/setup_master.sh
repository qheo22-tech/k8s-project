#!/bin/bash

# 에러 발생 시 즉시 중단 (안전장치)
set -e

echo "=========================================================="
echo "🚀 [K8S] 쿠버네티스 클러스터 통합 자동 설치를 시작합니다."
echo "=========================================================="

# 파일 권한 부여 (실행 가능하도록)
chmod +x 00_prepare_ansible.sh 01_master_init.sh 02_install_calico.sh 03_join_workers.sh

# ----------------------------------------------------------
# STEP 0: Ansible 및 SSH 환경 설정
# ----------------------------------------------------------
echo ""
echo ">>> [STEP 0] 앤서블 설치 및 SSH 보안 설정을 진행합니다..."
./00_prepare_ansible.sh

# ----------------------------------------------------------
# STEP 1: 마스터 노드 초기화 (Kubeadm Init)
# ----------------------------------------------------------
echo ""
echo ">>> [STEP 1] 마스터 노드 초기화를 시작합니다..."
./01_master_init.sh

# ----------------------------------------------------------
# STEP 2: Calico CNI 설치 (Pod 네트워크)
# ----------------------------------------------------------
echo ""
echo ">>> [STEP 2] Calico 네트워크 플러그인을 설치합니다..."
./02_install_calico.sh

# ----------------------------------------------------------
# STEP 3: 워커 노드 조인 (Ansible Playbook 실행)
# ----------------------------------------------------------
echo ""
echo ">>> [STEP 3] 워커 노드들을 클러스터에 연결합니다..."
echo "⚠️  주의: 워커 노드 접속을 위한 SSH 비밀번호 입력이 필요할 수 있습니다."
./03_join_workers.sh

# ----------------------------------------------------------
# 설치 완료 확인
# ----------------------------------------------------------
echo ""
echo "=========================================================="
echo "🎊 모든 설치 프로세스가 성공적으로 완료되었습니다!"
echo "=========================================================="
echo "현재 노드 상태를 확인합니다:"
kubectl get nodes

echo ""
echo "💡 팁: 모든 노드가 'Ready'가 될 때까지 1~2분 정도 소요될 수 있습니다."
echo "명령어 'watch kubectl get nodes'로 실시간 상태를 확인하세요."
echo "=========================================================="
